import java.util.Scanner;
public class Codeup_13 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();       // 정수 첫번째 입력
        int b = sc.nextInt();       // 정수 두번째 입력
        System.out.println(a + " " + b);        // 두개의 정수 사이에 공백
    }
}
